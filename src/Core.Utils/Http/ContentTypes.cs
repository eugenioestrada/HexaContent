namespace HexaContent.Core.Utils.Http;

/// <summary>
/// Provides constants for common content types.
/// </summary>
public class ContentTypes
{
	/// <summary>
	/// Represents the content type for JSON data.
	/// </summary>
	public const string ApplicationJson = "application/json";

	/// <summary>
	/// Represents the content type for binary data.
	/// </summary>
	public const string ApplicationOctetStream = "application/octet-stream";

	/// <summary>
	/// Represents the content type for PDF documents.
	/// </summary>
	public const string ApplicationPdf = "application/pdf";

	/// <summary>
	/// Represents the content type for XML data.
	/// </summary>
	public const string ApplicationXml = "application/xml";

	/// <summary>
	/// Represents the content type for ZIP archives.
	/// </summary>
	public const string ApplicationZip = "application/zip";

	/// <summary>
	/// Represents the content type for HTML documents.
	/// </summary>
	public const string TextHtml = "text/html";

	/// <summary>
	/// Represents the content type for CSS stylesheets.
	/// </summary>
	public const string TextCss = "text/css";

	/// <summary>
	/// Represents the content type for plain text.
	/// </summary>
	public const string TextPlain = "text/plain";

	/// <summary>
	/// Represents the content type for XML data in text format.
	/// </summary>
	public const string TextXml = "text/xml";
}
